package io.tima.app.platform

/**
 * Фоновая доставка вместо пушей через Google: держим своё WS-соединение живым, пока
 * приложение свёрнуто. Наружу не уходит ни текст сообщения, ни сам факт его получения.
 */
expect fun startBackgroundService()

/** Выход из аккаунта: держать соединение больше незачем. */
expect fun stopBackgroundService()

/** Нужен ли фоновый сервис на этой платформе (Android — да; Desktop окно и так живёт). */
expect fun backgroundSupported(): Boolean

/**
 * Жив ли сервис ПРЯМО СЕЙЧАС. Система может убить его при чистке памяти, и тогда
 * входящие молча перестают приходить — поэтому приложение проверяет и поднимает заново.
 */
expect fun backgroundServiceRunning(): Boolean

/**
 * Просит исключить приложение из оптимизации батареи. Без этого система усыпляет
 * соединение, и входящие приходят с задержкой или не приходят вовсе.
 * true — исключение уже есть (или платформе это не нужно).
 */
expect fun batteryOptimizationIgnored(): Boolean

/** Открыть системный экран отключения оптимизации батареи для этого приложения. */
expect fun requestIgnoreBatteryOptimization()
