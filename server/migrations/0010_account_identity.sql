-- 0010: ключ личности аккаунта (ADR-0010 §этап 3).
-- Долговременный Ed25519-ключ пользователя, выводимый из recovery-фразы (один на
-- аккаунт, стабилен между устройствами). Доказывает владение аккаунтом при
-- восстановлении истории — барьер против угона номера (укравший SIM пройдёт SMS,
-- но без фразы не подпишет запрос). NULL — пользователь без фразы (устройство работает,
-- но восстановление истории ему недоступно).
ALTER TABLE users
    ADD COLUMN IF NOT EXISTS identity_pub BYTEA
        CHECK (identity_pub IS NULL OR octet_length(identity_pub) = 32);
